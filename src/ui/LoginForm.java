    private void seconnecterbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_seconnecterbtnActionPerformed
        // TODO add your handling code here:
         String login = logintxt.getText().trim();
    String password = new String(passwordtxt.getPassword());

    if (login.isEmpty() || password.isEmpty()) {
        JOptionPane.showMessageDialog(
                this,
                "Veuillez saisir le login et le mot de passe.",
                "Erreur",
                JOptionPane.ERROR_MESSAGE
        );
        return; 
    }

    boolean ok = utilisateurService.checkLogin(login, password);

    if (ok) {
        JOptionPane.showMessageDialog(
                this,
                "Connexion réussie, bienvenue " + login + " !",
                "Succès",
                JOptionPane.INFORMATION_MESSAGE
        );

        Main m = new Main();   
        m.setVisible(true);
        this.dispose();
    } else {
        JOptionPane.showMessageDialog(
                this,
                "Login ou mot de passe incorrect.",
                "Erreur",
                JOptionPane.ERROR_MESSAGE
        );
    }
    }//GEN-LAST:event_seconnecterbtnActionPerformed

