    private void passwordoubliebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_passwordoubliebtnActionPerformed
        // TODO add your handling code here:
         String email = JOptionPane.showInputDialog(
            this,
            "Veuillez saisir votre adresse e-mail :",
            "Mot de passe oublié",
            JOptionPane.QUESTION_MESSAGE
    );

    if (email == null) {
        // L'utilisateur a cliqué sur Annuler
        return;
    }

    email = email.trim();
    if (email.isEmpty()) {
        JOptionPane.showMessageDialog(
                this,
                "Veuillez saisir une adresse e-mail.",
                "Erreur",
                JOptionPane.ERROR_MESSAGE
        );
        return;
    }

    try {
        boolean ok = utilisateurService.resetPassword(email);

        if (ok) {
            JOptionPane.showMessageDialog(
                    this,
                    "Un nouveau mot de passe temporaire a été envoyé à : " + email,
                    "Succès",
                    JOptionPane.INFORMATION_MESSAGE
            );
        } else {
            JOptionPane.showMessageDialog(
                    this,
                    "Aucun utilisateur n'est enregistré avec cet e-mail.",
                    "Erreur",
                    JOptionPane.ERROR_MESSAGE
            );
        }
    } catch (Exception ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(
                this,
                "Une erreur s'est produite lors de l'envoi de l'e-mail.\n" + ex.getMessage(),
                "Erreur",
                JOptionPane.ERROR_MESSAGE
        );
    }
    }//GEN-LAST:event_passwordoubliebtnActionPerformed

    private void inscrirebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inscrirebtnActionPerformed
        // TODO add your handling code here:
        JTextField loginField = new JTextField();
        JTextField emailField = new JTextField();
        JPasswordField passwordField = new JPasswordField();
        JPasswordField confirmField = new JPasswordField();

    Object[] message = {
        "Login :", loginField,
        "Email :", emailField,
        "Mot de passe :", passwordField,
        "Confirmer le mot de passe :", confirmField
    };

    int option = JOptionPane.showConfirmDialog(
            this,
            message,
            "Créer un nouveau compte",
            JOptionPane.OK_CANCEL_OPTION
    );

    if (option != JOptionPane.OK_OPTION) {
        return; 
    }

    String login = loginField.getText().trim();
    String email = emailField.getText().trim();
    String password = new String(passwordField.getPassword());
    String confirm = new String(confirmField.getPassword());

    // Validations simples
    if (login.isEmpty() || email.isEmpty() || password.isEmpty() || confirm.isEmpty()) {
        JOptionPane.showMessageDialog(
                this,
                "Veuillez remplir tous les champs.",
                "Erreur",
                JOptionPane.ERROR_MESSAGE
        );
        return;
    }

    if (!password.equals(confirm)) {
        JOptionPane.showMessageDialog(
                this,
                "Les mots de passe ne correspondent pas.",
                "Erreur",
                JOptionPane.ERROR_MESSAGE
        );
        return;
    }

    // Appel au service
    boolean ok = utilisateurService.register(login, email, password);

    if (!ok) {
        JOptionPane.showMessageDialog(
                this,
                "Login ou e-mail déjà utilisé.",
                "Erreur",
                JOptionPane.ERROR_MESSAGE
        );
    } else {
        JOptionPane.showMessageDialog(
                this,
                "Compte créé avec succès !\nVous pouvez maintenant vous connecter.",
                "Succès",
                JOptionPane.INFORMATION_MESSAGE
        );
    }
    }//GEN-LAST:event_inscrirebtnActionPerformed

        
