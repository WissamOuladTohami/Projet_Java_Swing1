    private void ajouterbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ajouterbtnActionPerformed
        // TODO add your handling code here:
        
       double prix = Double.parseDouble(prixtxt.getText());
        ps.create(new ProduitAgro( nomtxt.getText(), typetxt.getText(), prix));
        vider();
        load();
    }//GEN-LAST:event_ajouterbtnActionPerformed

    private void supprimerbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_supprimerbtnActionPerformed
              
        int reponse = JOptionPane.showConfirmDialog(this,"Voulez-vous vraiment supprimer ce Produit ?");
        
        if(reponse == 0){
            ps.delete(ps.findById(id));
            
            load();
            vider();
        }
    }//GEN-LAST:event_supprimerbtnActionPerformed

    private void listeproduitsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listeproduitsMouseClicked
        // TODO add your handling code here:
        id = Integer.parseInt(model.getValueAt(listeproduits.getSelectedRow(), 0).toString());
        nomtxt.setText(model.getValueAt(listeproduits.getSelectedRow(), 1).toString());
        typetxt.setText(model.getValueAt(listeproduits.getSelectedRow(), 2).toString());
        prixtxt.setText(model.getValueAt(listeproduits.getSelectedRow(), 3).toString());
    }//GEN-LAST:event_listeproduitsMouseClicked

    private void modifierbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modifierbtnActionPerformed
        // TODO add your handling code here:
         int reponse = JOptionPane.showConfirmDialog(this,"Voulez-vous vraiment modifier cet Agriculteur ?");
        
        if(reponse == 0){
            double prix = Double.parseDouble(prixtxt.getText());
            ps.update(new ProduitAgro(id, nomtxt.getText(), typetxt.getText(), prix));
            load();
            vider();
        }
    }//GEN-LAST:event_modifierbtnActionPerformed


