    public void load() {
        model.setRowCount(0);
        for (ProduitAgro p : ps.findAll()) {
            model.addRow(new Object[]{
                p.getId(),
                p.getNom(),
                p.getType(),
                p.getPrixKg()
            });
        }
    }
    
