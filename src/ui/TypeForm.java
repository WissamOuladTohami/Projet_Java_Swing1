    private void loadTypes() {
        typecombo.removeAllItems();
        typecombo.addItem("Tous");

        List<String> types = ps.findAllTypes();  

        if (types != null) {
            for (String t : types) {
                typecombo.addItem(t);
            }
        }
    }
    
    private void loadByType(String type) {
    model.setRowCount(0);
    List<VenteAgro> ventes = vs.findAll();  

    for (VenteAgro v : ventes) {
        String typeProduit = v.getProduit().getType();
        if (typeProduit == null) continue;

        
        if (!typeProduit.trim().equalsIgnoreCase(type.trim())) {
            continue;
        }

        model.addRow(new Object[]{
            v.getId(),
            v.getProduit().getNom(),
            typeProduit,
            v.getAgriculteur().getNom(),
            v.getAgriculteur().getCommune()
        });
    }
}
    
    private void loadAll() {
        model.setRowCount(0);
        List<VenteAgro> ventes = vs.findAll();

        for (VenteAgro v : ventes) {
            model.addRow(new Object[]{
                v.getId(),
                v.getProduit().getNom(),
                v.getProduit().getType(),          
                v.getAgriculteur().getNom(),
                v.getAgriculteur().getCommune()
            });
        }
    }

